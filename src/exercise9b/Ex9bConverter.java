public class Ex9bConverter {
    public Double mi2km(Double valueFrom) {
      return valueFrom * 1.609;
  }
    public Double km2mi(Double valueFrom) {
      return valueFrom / 1.609;
  }
    public Double insq2cmsq(Double valueFrom) {
      return valueFrom * 6.452;
  }
    public Double cmsq2insq(Double valueFrom) {
      return valueFrom / 6.452;
  }
    public Double floz2ml(Double valueFrom) {
      return valueFrom * 29.5735;
  }
    public Double ml2floz(Double valueFrom) {
      return valueFrom / 29.5735;
  }
    public Double lb2kg(Double valueFrom) {
      return valueFrom * 0.453592;
  }
    public Double kg2lb(Double valueFrom) {
      return valueFrom / 0.453592;
  }
    public Double pa2torr(Double valueFrom) {
      return valueFrom * 0.00750061683;
  }
    public Double torr2pa(Double valueFrom) {
      return valueFrom * 133.322368;
  }
    
}
